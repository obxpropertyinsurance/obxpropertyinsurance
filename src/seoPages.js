export const seoPages = [...coreSeoPages, ...townSeoPages].map(enhanceSeoPage);

export function getSeoPageByPath(pathname) {
  const slug = pathname.replace(/^\/+|\/+$/g, "");
  return seoPages.find((page) => page.slug === slug) || null;
}
